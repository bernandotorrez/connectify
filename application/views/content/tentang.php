    					<h2 class="title">Sejarah Jujitsu</h2>
    					<h5 class="">
 <p style="text-align: justify;">Jujitsu pada dasarnya adalah bentuk-bentuk pembelaan diri yang bersifat defensif dan memanfaatkan "Yawara-gi" atau teknik-teknik yang bersifat fleksibel, di mana serangan dari lawan tidak dihadapi dengan kekuatan, melainkan dengan cara "menipu" lawan agar daya serangan tersebut dapat digunakan untuk mengalahkan dirinya sendiri. Dari seni beladiri Jujitsu ini, lahirlah beberapa seni beladiri lainnya yang mempunyai konsep defensif serupa, yaitu Aikido dan Judo, keduanya juga berasal dari Jepang.</p>
 <p style="text-align: justify;">Jujitsu terdiri atas bermacam-macam aliran (Ryuha), namun pada garis besarnya terbagi atas dua "gaya", yaitu tradisional dan modern. Gerakan dari kedua macam "gaya" Jujitsu ini adalah hampir sama, namun jurus-jurus Jujitsu modern sudah disesuaikan dengan situasi pembelaan diri pada zaman modern, sedangkan jurus-jurus Jujitsu tradisional biasanya mencerminkan situasi pembelaan diri di saat aliran Jujitsu yang bersangkutan diciptakan. Sebagai contoh, Jujitsu yang diciptakan pada zaman Sengoku Jidai (sebelum Shogun Tokugawa berkuasa) menekankan pada pertarungan di medan perang dengan memakai baju besi (disebut Yoroi Kumi Uchi), sedangkan yang diciptakan pada zaman Edo (sesudah Shogun Tokugawa berkuasa) menekankan pada beladiri dengan memakai pakaian sehari-hari (Suhada Jujitsu).</p>
 <p style="text-align: justify;">Teknik-teknik Jujitsu pada garis besarnya terdiri atas atemi waza (menyerang bagian yang lemah dari tubuh lawan), kansetsu waza/gyakudori (mengunci persendian lawan) dan nage waza (menjatuhkan lawan). Setiap aliran Jujitsu memiliki caranya sendiri untuk melakukan teknik-teknik tersebut di atas. Teknik-teknik tersebut lahir dari metode pembelaan diri kaum Samurai (prajurit perang zaman dahulu) di saat mereka kehilangan pedangnya, atau tidak ingin menggunakan pedangnya (misalnya karena tidak ingin melukai atau membunuh lawan).</p>
  <p style="text-align: justify;">Aliran Jujitsu yang tertua di Jepang adalah Takenouchi-ryu yang didirikan tahun 1532 oleh Pangeran Takenouchi Hisamori. Aliran-aliran lain yang terkenal antara lain adalah Shindo Yoshin-ryu yang didirikan oleh Matsuoka Katsunosuke pada tahun 1864, Daito-ryu yang didirikan oleh Takeda Sokaku pada tahun 1892, Hakko-ryu yang didirikan Okuyama Ryuho pada tahun 1942, dan banyak aliran lainnya.</p>


